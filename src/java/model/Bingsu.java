package model;

public class Bingsu {
    private String name;
    private String type;
    private String Sweet;
    private int price;
    private String bingsu;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getSweet() {
        return Sweet;
    }

    public void setSweet(String Sweet) {
        this.Sweet = Sweet;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public String getBingsu() {
        return bingsu;
    }

    public void setBingsu(String bingsu) {
        this.bingsu = bingsu;
    }
}
    
